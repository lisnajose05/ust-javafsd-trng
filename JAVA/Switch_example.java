package com.ust;

public class Switch_example {
	public static void main(String args[])
	{
		String str="Green";
		switch(str) {
		case "Red":
			System.out.println("I am in Red");
			System.out.println("*********");
			
		default:
			System.out.println("default");
			break;
		case "Green":
			System.out.println("I am in Green");
			System.out.println("**********");
			break;
			
		case "Yellow":
			System.out.println("I am in Yellow");
			System.out.println("***********");
			break;
			
		
			
			
		
		}
	}

}
